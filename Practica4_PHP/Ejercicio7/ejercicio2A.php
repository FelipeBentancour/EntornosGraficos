<?php
// Se crea un array asociativo llamado $matriz con dos elementos:
// La clave "x" tiene el valor "bar"
// La clave 12 tiene el valor true
$matriz = array("x" => "bar", 12 => true);

// Imprime el valor asociado a la clave "x", que es "bar"
echo $matriz["x"];

// Imprime el valor asociado a la clave 12, que es true
// En PHP, true al imprimirse se muestra como "1"
echo $matriz[12];